package com.example.generals.fitbitmoniteringapplicationfinal;

public class History {

    private int idHistory;
    private String maladie;
    private String maladieLogo;
    private int pnn50;

    public int getPnn50() {
        return pnn50;
    }

    public void setPnn50(int pnn50) {
        this.pnn50 = pnn50;
    }

    public int getIdHistory() {
        return idHistory;
    }

    public void setIdHistory(int idHistory) {
        this.idHistory = idHistory;
    }

    public String getMaladie() {
        return maladie;
    }

    public void setMaladie(String maladie) {
        this.maladie = maladie;
    }

    public String getMaladieLogo() {
        return maladieLogo;
    }

    public void setMaladieLogo(String maladieLogo) {
        this.maladieLogo = maladieLogo;
    }
}
